function addition(n1, n2){
    let res=n1+n2
    console.log(n1+"+"+n2+"="+res)
}

function subtract(n1, n2){
    const res=n1-n2
    console.log(n1+"-"+n2+"="+res)
}

function multiply(n1, n2){
    const res=n1*n2
    console.log(n1+"*"+n2+"="+res)
}

function divide(n1, n2){
    const res=n1/n2
    console.log(n1+"/"+n2+"="+res)
}
addition(10,20)
subtract(10,5)
multiply(10,5)
divide(10,5)
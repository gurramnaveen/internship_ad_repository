SET SERVEROUTPUT ON;
--******************************************************************************
--PRINTING HELLO WORLD
BEGIN
    DBMS_OUTPUT.put_line('HELLO WORLD!');
END;

--******************************************************************************
-- USING VARIABLE
DECLARE
    l_message VARCHAR2(255) := 'HELLO WORLD!';
BEGIN
    DBMS_OUTPUT.PUT_LINE(l_message);
END;

--******************************************************************************
-- ERROR/EXCEPTION HANDLING
DECLARE
    v_result NUMBER;
BEGIN
    v_result := 1/0;
    EXCEPTION
        WHEN ZERO_DIVIDE THEN
            DBMS_OUTPUT.PUT_LINE(SQLERRM);
END;
